class ReversePatch(nn.Module):

    def __init__(self, input_resolution, embed_dim, outplanes, act_layer=nn.GELU, norm_layer=nn.LayerNorm):
        super().__init__()
        self.input_resolution = input_resolution
        self.linear = nn.Linear(embed_dim, outplanes, bias=False)
        self.norm = norm_layer(outplanes)
        self.act = act_layer()
        self.outplanes = outplanes

    def forward(self, x):
        """
        x: B, H*W, C
        """
        B, _, C = x.shape
        H, W = self.input_resolution
        x = self.linear(x)
        x = self.norm(x)
        x = self.act(x)
        x = x.transpose(1, 2).reshape(B, self.outplanes, H, W)

        return x


class Attention(nn.Module):
    def __init__(self, in_planes, out_planes, groups=16, kernel_size=56,  # 这是AxialAttention类的初始化方法。它需要几个参数：输入通道数，输出通道数，分组数，卷积核大小，步长，偏置，宽度。
                 stride=1, bias=False, width=False,):
        assert (in_planes % groups == 0) and (out_planes % groups == 0) # 断言输入通道数和输出通道数可以被组数整除
        super(Attention, self).__init__()  # 调用父类的初始化方法

        self.in_planes = in_planes # 输入通道数。这是一个标量
        self.out_planes = out_planes # 输出通道数。这是一个标量
        self.groups = groups   # 划分输入和输出平面的组数。它默认为 8，这意味着输入和输出平面将被分成 8 个组。
        self.group_planes = out_planes // groups # 每个组的输出平面数。这是输出平面数除以组数的结果。
        self.kernel_size = kernel_size # 卷积核大小。这是一个标量
        self.stride = stride # 步长。这是一个标量
        self.bias = bias
        self.width = width
        self.num_layers = num_layers



        # 初始化可学习的权重矩阵
        self.qr = nn.Parameter(torch.randn(num_layers, embed_dim, embed_dim))
        self.kr = nn.Parameter(torch.randn(num_layers, embed_dim, embed_dim))
        self.vr = nn.Parameter(torch.randn(num_layers, embed_dim, embed_dim))

        # 初始化卷积层
        self.cnnblock = ConvNeXt(in_planes=in_planes, out_planes=out_planes)
        self.transblock = ReversePatch(input_resolution, embed_dim, out_planes)


    def forward(self, x):
        # 第一部分：计算注意力值y1
        q = x  # 作为查询
        k = x  # 作为键
        v = x  # 作为值
        y1_list = []
        for i in range(self.num_layers):
            qr = self.qr[i].unsqueeze(0)
            kr = self.kr[i].unsqueeze(0)
            vr = self.vr[i].unsqueeze(0)
            qr_inv = torch.inverse(qr)
            kr_inv = torch.inverse(kr)

            # 计算注意力值y1
            y1 = F.softmax(torch.matmul(torch.matmul(q, qr_inv) + torch.matmul(q, kr_inv), v + vr), dim=-1)
            y1_list.append(y1)

        # 第二部分：计算最终的注意力值y
        y1_stack = torch.stack(y1_list, dim=0)
        q_y1 = torch.matmul(q.unsqueeze(0), y1_stack.permute(1, 0, 2, 3))  # 将y1作为查询
        k_cnn = [self.cnn_branches[i](x) for i in range(self.num_layers)]  # 使用CNN分支进行特征嵌入
        k_stack = torch.stack(k_cnn, dim=0)  # 将CNN分支特征嵌入堆叠
        k_stack_inv = torch.inverse(k_stack)  # 计算k的逆矩阵
        y = F.softmax(torch.matmul(torch.matmul(q_y1, k_stack_inv), v + vr), dim=-1)

        return y




